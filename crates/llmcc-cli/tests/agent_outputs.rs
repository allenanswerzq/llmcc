#[test]
fn pagerank_downranks_generated_code_by_default() {
    let temp = tempfile::tempdir().expect("create tempdir");
    fs::create_dir_all(temp.path().join("cmd/app")).expect("create cmd");
    fs::create_dir_all(temp.path().join("gen/client")).expect("create gen");
    fs::write(
        temp.path().join("cmd/app/main.go"),
        "package main\n\nfunc main() { Start() }\nfunc Start() { DecodeGenerated() }\n",
    )
    .expect("write main");
    fs::write(
        temp.path().join("gen/client/encode_decode.go"),
        "package main\n\n// Code generated by fixture. DO NOT EDIT.\nfunc DecodeGenerated() {}\n",
    )
    .expect("write generated");

    let output = run_lang_fixture(temp.path(), "go", &["--pagerank-top-k", "1"]);

    assert!(
        output.status.success(),
        "status: {:?}\nstderr:\n{}",
        output.status,
        stderr(&output)
    );
    let out = stdout(&output);
    assert!(out.contains("Start") || out.contains("main"), "{out}");
    assert!(!out.contains("DecodeGenerated"), "{out}");
}

#[test]
fn json_output_has_stable_agent_graph_schema() {
    let temp = fixture();
    let output = run_fixture(temp.path(), &["--format", "json"]);

    assert!(
        output.status.success(),
        "status: {:?}\nstderr:\n{}",
        output.status,
        stderr(&output)
    );
    let graph: Value = serde_json::from_slice(&output.stdout).expect("json output");
    assert_eq!(graph["schema_version"], 1);
    let nodes = graph["nodes"].as_array().expect("nodes array");
    assert!(!nodes.is_empty());
    let pagerank = graph["pagerank"].as_array().expect("pagerank array");
    assert!(pagerank.is_empty());

    let node_ids: std::collections::HashSet<_> = nodes
        .iter()
        .map(|node| node["id"].as_str().expect("node id"))
        .collect();
    for node in nodes {
        for key in [
            "id",
            "unit_index",
            "block_id",
            "name",
            "block_kind",
            "sym_kind",
            "location",
            "file_path",
            "line_start",
            "crate_name",
            "module_path",
            "is_exported",
        ] {
            assert!(node.get(key).is_some(), "missing key {key} in {node:?}");
        }
    }
    for edge in graph["edges"].as_array().expect("edges array") {
        assert!(node_ids.contains(edge["from"].as_str().expect("edge from")));
        assert!(node_ids.contains(edge["to"].as_str().expect("edge to")));
    }
}

#[test]
fn markdown_agent_summary_reports_sections_and_public_api() {
    let temp = fixture();
    let output = run_fixture(
        temp.path(),
        &[
            "--format",
            "markdown",
            "--agent-summary",
            "--pagerank-top-k",
            "20",
        ],
    );

    assert!(
        output.status.success(),
        "status: {:?}\nstderr:\n{}",
        output.status,
        stderr(&output)
    );
    let out = stdout(&output);
    for heading in [
        "Top Symbols",
        "Public API Surface",
        "Caller Callee Clusters",
        "Cross File Coupling",
        "Likely Refactor Entry Points",
        "Inferred Tests",
    ] {
        assert!(out.contains(heading), "missing heading {heading}\n{out}");
    }
    let public_api = out
        .split("## Public API Surface")
        .nth(1)
        .and_then(|s| s.split("## Caller Callee Clusters").next())
        .expect("public api section");
    assert!(public_api.contains("entry"));
    assert!(public_api.contains("helper"));
    assert!(!public_api.contains("private_helper"));
}

