use std::fs;
use std::path::Path;
use std::process::Command;

use serde_json::Value;
use tempfile::TempDir;

fn fixture() -> TempDir {
    let temp = tempfile::tempdir().expect("create tempdir");
    write_fixture(temp.path());
    temp
}

fn write_fixture(root: &Path) {
    fs::create_dir_all(root.join("src")).expect("create src");
    fs::create_dir_all(root.join("tests")).expect("create tests");
    fs::write(
        root.join("src/lib.rs"),
        "pub mod util;\npub fn entry() { util::helper(); }\n",
    )
    .expect("write lib");
    fs::write(
        root.join("src/util.rs"),
        "pub fn helper() { private_helper(); }\nfn private_helper() {}\n",
    )
    .expect("write util");
    fs::write(
        root.join("tests/entry_test.rs"),
        "#[test]\nfn entry_test() { fixture::entry(); }\n",
    )
    .expect("write test");
}

fn llmcc() -> Command {
    Command::new(env!("CARGO_BIN_EXE_llmcc"))
}

fn run_fixture(root: &Path, args: &[&str]) -> std::process::Output {
    let mut cmd = llmcc();
    cmd.args(["--lang", "rust", "--dir"])
        .arg(root)
        .args(args)
        .output()
        .expect("run llmcc")
}

fn run_lang_fixture(root: &Path, lang: &str, args: &[&str]) -> std::process::Output {
    let mut cmd = llmcc();
    cmd.args(["--lang", lang, "--dir"])
        .arg(root)
        .args(args)
        .output()
        .expect("run llmcc")
}

fn stdout(output: &std::process::Output) -> String {
    String::from_utf8(output.stdout.clone()).expect("stdout utf8")
}

fn stderr(output: &std::process::Output) -> String {
    String::from_utf8(output.stderr.clone()).expect("stderr utf8")
}

