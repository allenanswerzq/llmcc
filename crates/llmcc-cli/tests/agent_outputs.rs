#[test]
fn git_diff_agent_summary_marks_changed_files() {
    let temp = fixture();
    let init = Command::new("git")
        .args(["init"])
        .current_dir(temp.path())
        .output()
        .expect("git init");
    assert!(init.status.success(), "{}", stderr(&init));
    let add = Command::new("git")
        .args(["add", "."])
        .current_dir(temp.path())
        .output()
        .expect("git add");
    assert!(add.status.success(), "{}", stderr(&add));
    let commit = Command::new("git")
        .args([
            "-c",
            "user.email=test@example.com",
            "-c",
            "user.name=Test",
            "commit",
            "-m",
            "init",
        ])
        .current_dir(temp.path())
        .output()
        .expect("git commit");
    assert!(commit.status.success(), "{}", stderr(&commit));
    fs::write(
        temp.path().join("src/lib.rs"),
        "pub mod util;\npub fn entry() { util::helper(); }\npub fn changed() {}\n",
    )
    .expect("modify lib");

    let output = run_fixture(
        temp.path(),
        &["--git-diff", "--agent-summary", "--format", "markdown"],
    );

    assert!(
        output.status.success(),
        "status: {:?}\nstderr:\n{}",
        output.status,
        stderr(&output)
    );
    let out = stdout(&output);
    assert!(out.contains("Changed Files"));
    assert!(out.contains("src/lib.rs"));
    let changed = out
        .split("## Changed Files")
        .nth(1)
        .and_then(|s| s.split("## Top Symbols").next())
        .expect("changed files section");
    assert!(changed.contains("src/util.rs"));
    assert!(!changed.contains("pagerank_total=0.000000"));

    let public_api = out
        .split("## Public API Surface")
        .nth(1)
        .and_then(|s| s.split("## Caller Callee Clusters").next())
        .expect("public api section");
    assert!(public_api.contains("entry"));
    assert!(!public_api.contains("helper"));
    assert!(!public_api.contains("src/util.rs"));
}
